class Calculos{
    suma = (a , b) => a + b;
    resta = (a , b) => a - b;
    division = (a , b) => a / b;
    multiplicacion = (a , b) => a * b;
    raiz = (a) => Math.sqrt(a);
    potenciaAuto = (a) => Math.pow(a,2);
    potencia = (a,b) => Math.pow(a,b);
    PI = (a) => a * Math.PI;
}
